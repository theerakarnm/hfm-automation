import type { ReportDashboardResponse } from "@/types/report"

function makeMetricDelta(
  value: number,
  previousValue: number,
  format: ReportDashboardResponse["summary"]["currentWallets"]["format"],
  tone: ReportDashboardResponse["summary"]["currentWallets"]["tone"]
) {
  const delta = value - previousValue
  const deltaPercent =
    previousValue !== 0 ? Math.round((delta / previousValue) * 10000) / 100 : 0
  const direction =
    delta > 0
      ? ("up" as const)
      : delta < 0
        ? ("down" as const)
        : ("flat" as const)
  return { value, previousValue, delta, deltaPercent, direction, tone, format }
}

